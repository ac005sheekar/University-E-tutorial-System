Hardware<br>
Every general purpose computer consists of the hardware, operating system, system programs, and application programs. The hardware consists of memory, CPU, ALU, and I/O devices, peripheral device and storage device. System program consists of compilers, loaders, editors, OS etc. The application program consists of business programs, database programs.<br><br>


Every computer must have an operating system to run other programs. The operating system coordinates the use of the hardware among the various system programs and application programs for a various users. It simply provides an environment within which other programs can do useful work.<br><br>

The operating system is a set of special programs that run on a computer system that allows it to work properly. It performs basic tasks such as recognizing input from the keyboard, keeping track of files and directories on the disk, sending output to the display screen and controlling peripheral devices.<br><br>
OS is designed to serve two basic purposes:

It controls the allocation and use of the computing System’s resources among the various user and tasks.<br>
It provides an interface between the computer hardware and the programmer that simplifies and makes feasible for coding, creation, debugging of application programs.<br>
The Operating system must support the following tasks. The task are:<br>

Provides the facilities to create, modification of programs and data files using an editor.<br>
Access to the compiler for translating the user program from high level language to machine language.<br>
Provide a loader program to move the compiled program code to the computer’s memory for execution.<br>
Provide routines that handle the details of I/O programming.<br>
I/O System Management –<br>
The module that keeps track of the status of devices is called the I/O traffic controller. Each I/O device has a device handler that resides in a separate process associated with that device.<br><br>
The I/O subsystem consists of<br><br>

A memory Management component that includes buffering caching and spooling.<br>
A general device driver interface.<br>
Drivers for specific hardware devices.<br>

Assembler –<br>
Input to an assembler is an assembly language program. Output is an object program plus information that enables the loader to prepare the object program for execution. At one time, the computer programmer had at his disposal a basic machine that interpreted, through hardware, certain fundamental instructions. He would program this computer by writing a series of ones and Zeros (Machine language), place them into the memory of the machine.<br><br>

Compiler –<br>
The High level languages- examples are FORTRAN, COBAL, ALGOL and PL/I are processed by compilers and interpreters. A compiler is a program that accepts a source program in a “high-level language “and produces a corresponding object program. An interpreter is a program that appears to execute a source program as if it was machine language. The same name (FORTRAN, COBAL etc.) is often used designate both a compiler and its associated language.<br><br>

Loader –<br>
A Loader is a routine that loads an object program and prepares it for execution. There are various loading schemes: absolute, relocating and direct-linking. In general, the loader must load, relocate and link the object program. Loader is a program that places programs into memory and prepares them for execution. In a simple loading scheme, the assembler outputs the machine language translation of a program on a secondary devices and a loader is places in core. The loader places into memory the machine language version of the user’s program and transfers control to it. Since the loader program is much smaller than the assembler, those makes more core available to user’s program.<br><br>

