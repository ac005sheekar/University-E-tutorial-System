<h2>Operating System | Introduction of Operating System Set 1</h2><br><br><br>



An operating system acts as an intermediary between the user of a computer and computer hardware. The purpose of an operating system is to provide an environment in which a user can execute programs in a convenient and efficient manner.<br><br>

An operating system is software that manages the computer hardware. The hardware must provide appropriate mechanisms to ensure the correct operation of the computer system and to prevent user programs from interfering with the proper operation of the system.<br><br>

Operating System – Definition:<br><br><br>



 

An operating system is a program that controls the execution of application programs and acts as an interface between the user of a computer and the computer hardware.<br>
A more common definition is that the operating system is the one program running at all times on the computer (usually called the kernel), with all else being application programs.<br>
An operating system is concerned with the allocation of resources and services, such as memory, processors, devices and information. The operating system correspondingly includes programs to manage these resources, such as a traffic controller, a scheduler, memory management module, I/O programs, and a file system.<br>
Functions of Operating system – Operating system performs three functions:<br><br>

Convenience: An OS makes a computer more convenient to use.<br><br>
Efficiency: An OS allows the computer system resources to be used in an efficient manner.<br>
Ability to Evolve: An OS should be constructed in such a way as to permit the effective development, testing and introduction of new system functions without at the same time interfering with service.<br>
